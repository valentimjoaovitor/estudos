'''
2.8 Extensões de arquivo: O Python tem um método removesuffix() que funciona exatamente como removeprefix().
Atribua o valor 'python_notes.txt' a uma váriavel chamada filename. Depois, utilize o método removesuffix()
para exibir o nome do arquivo sem a extensão do arquivo, como alguns navegadores de arquivos fazem.

'''

filename = 'python_notes.txt'
print(filename.removesuffix('.txt'))
